package info;

public class UserInfo {
	
	private String id;
	private int stage;
	private int recordTime;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getStage() {
		return stage;
	}
	public void setStage(int stage) {
		this.stage = stage;
	}
	public int getRecordTime() {
		return recordTime;
	}
	public void setRecordTime(int recordTime) {
		this.recordTime = recordTime;
	}
	
	

}
